//switch case
const expr = 'Papayas';
switch (expr){
    case 'oranges':
        console.log("oranges are sweet and sour.");
        break;
    case 'Mangoes':
        console.log("Mango is my favourite fruit");
        break;
    case 'Papayas':
        console.log("Papayas are very rich fruits");
        break;
    default:
        console.log("invalid choice!");
}
